import medvoll.spring3_alura.domain.paciente.DadosDetalhamentoPaciente;
import medvoll.spring3_alura.domain.paciente.DadosListagemPaciente;
import medvoll.spring3_alura.domain.paciente.Paciente;
import medvoll.spring3_alura.domain.paciente.PacienteRepository;
